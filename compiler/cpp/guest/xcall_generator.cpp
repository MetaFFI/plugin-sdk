void CppXcallGenerator::print_compile_commands(const std::string& stem) {
    std::cout << "\n"
              << "// =================================================================\n"
              << "// Compile commands for " << stem << ".cpp\n"
              << "// =================================================================\n"
              << "\n"
              << "// Windows (cl.exe):\n"
              << "//   cl /LD /EHsc " << stem << ".cpp"
              << " /I%METAFFI_HOME%\\include /link /OUT:" << stem << ".dll\n"
              << "\n"
              << "// Linux (g++):\n"
              << "//   g++ -shared -fPIC -o " << stem << ".so " << stem << ".cpp \\\n"
              << "//       -I$METAFFI_HOME/include -Wl,-rpath,$ORIGIN\n"
              << "\n"
              << "// macOS (clang++):\n"
              << "//   clang++ -shared -fPIC -o " << stem << ".dylib " << stem << ".cpp \\\n"
              << "//       -I$METAFFI_HOME/include\n"
              << "\n";
}

