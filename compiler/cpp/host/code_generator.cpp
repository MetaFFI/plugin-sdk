std::string CppCodeGenerator::params_decl(const std::vector<metaffi::idl::ArgDefinition>& params) const {
    std::string result;

    for (size_t i = 0; i < params.size(); ++i) {
        if (i > 0) result += ", ";
        result += CppTypeMapper::cpp_type(params[i].type(), params[i].dimensions());
        result += " ";
        result += to_ident(params[i].name());
    }

    return result;
}

std::string CppCodeGenerator::params_names(const std::vector<metaffi::idl::ArgDefinition>& params) const {
    std::string result;

    for (size_t i = 0; i < params.size(); ++i) {
        if (i > 0) result += ", ";
        result += to_ident(params[i].name());
    }

    return result;
}

std::string CppCodeGenerator::type_info_list(const std::vector<metaffi::idl::ArgDefinition>& args) const {
    if (args.empty()) {
        return "{}";
    }

    std::string result = "{";
    for (size_t i = 0; i < args.size(); ++i) {
        if (i > 0) result += ", ";
        result += CppTypeMapper::type_info_literal(args[i].type(), args[i].dimensions());
    }
    result += "}";
    return result;
}

std::string CppCodeGenerator::call_expression(const std::string& ev,
                                               const std::vector<metaffi::idl::ArgDefinition>& params,
                                               const std::vector<metaffi::idl::ArgDefinition>& returns,
                                               bool instance_first) const {
    // Build argument string
    std::string args;
    if (instance_first) {
        args = "_handle";
        if (!params.empty()) {
            args += ", " + params_names(params);
        }
    } else {
        args = params_names(params);
    }

    // Build call based on number of return values
    if (returns.empty()) {
        // Void call
        return ev + "->call(" + args + ");";
    }

    if (returns.size() == 1) {
        // Single return — structured binding
        const std::string ret_type = CppTypeMapper::cpp_type(returns[0].type(), returns[0].dimensions());
        const std::string ret_name = to_ident(returns[0].name());
        return "auto [" + ret_name + "] = " + ev + "->call<" + ret_type + ">(" + args + ");\n"
               + "    return " + ret_name + ";";
    }

    // Multiple returns — structured binding + tuple
    std::string binding_names;
    std::string ret_types;
    for (size_t i = 0; i < returns.size(); ++i) {
        if (i > 0) {
            binding_names += ", ";
            ret_types += ", ";
        }
        binding_names += to_ident(returns[i].name());
        ret_types += CppTypeMapper::cpp_type(returns[i].type(), returns[i].dimensions());
    }

    return "auto [" + binding_names + "] = " + ev + "->call<" + ret_types + ">(" + args + ");\n"
           + "    return std::make_tuple(" + binding_names + ");";
}

std::string CppCodeGenerator::entity_var(const std::string& class_name,
                                          const std::string& entity_label) {
    std::string var = "_";
    if (!class_name.empty()) {
        var += to_ident(class_name) + "_";
    }
    var += to_ident(entity_label) + "_entity";
    return var;
}

std::vector<metaffi::idl::ArgDefinition>
CppCodeGenerator::filter_instance_params(const std::vector<metaffi::idl::ArgDefinition>& params,
                                          bool instance_required) {
    // When instance_required is true the IDL always places the handle as the first
    // parameter.  Strip it — the C++ method uses _handle implicitly instead.
    if (!instance_required || params.empty()) {
        return params;
    }
    if (params[0].type() == "handle") {
        return std::vector<metaffi::idl::ArgDefinition>(params.begin() + 1, params.end());
    }
    return params;
}

std::string CppCodeGenerator::to_ident(const std::string& s) {
    std::string result = s;
    // Replace characters not valid in C++ identifiers with underscores
    for (char& c : result) {
        if (!std::isalnum(static_cast<unsigned char>(c)) && c != '_') {
            c = '_';
        }
    }
    return result;
}

